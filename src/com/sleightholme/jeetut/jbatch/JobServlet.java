@WebServlet(name = "JobServlet", urlPatterns = {"/JobServlet"})
public class JobServlet extends ExtendedServlet {
    
    @Override
    public void init(ServletConfig config) throws ServletException{
        title = "JBatch Job Starter Servlet";
        super.init(config);
    }

    
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        header();
        out.println("Running batch job...");
        JobOperator jobop = BatchRuntime.getJobOperator();
        Properties props = new Properties();
        long jobid = jobop.start("batchprocess", props);
        out.print(jobid);
        newLine();
        out.println("Finished results avaliable <a href=\"JobEnd\">here</a>");
        
        footer();
    }

}
